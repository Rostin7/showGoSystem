import request from '@/utils/request'

export function searchUser(name) {
  return request({
    url: '/search/user',
    method: 'get',
    params: { name }
  })
}

export function transactionList(query) {
  // console.log(22222222222222222222222222222)
  return request({
    url: '/transaction/list',
    method: 'get',
    params: query
  })
}
